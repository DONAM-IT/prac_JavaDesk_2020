    public void viewProduct() {
        try {
            BikeDAO dao = new BikeDAO();
            java.util.List<BikeDTO> result = dao.getBikeList();
            showProduct(result);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void showProduct(java.util.List<BikeDTO> list) throws Exception {
        tblModelBike.setRowCount(0);
        for (BikeDTO bikeDTO : list) {
            tblModelBike.addRow(bikeDTO.toVector());
        }
    }

