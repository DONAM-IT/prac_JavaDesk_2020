    private void tblViewBikeMouseClicked(MouseEvent evt) {//GEN-FIRST:event_tblViewBikeMouseClicked
        // TODO add your handling code here:
        int row = tblViewBike.getSelectedRow();
        String proId = (String) tblModelBike.getValueAt(row, 0);

        try {
            BikeDAO dao = new BikeDAO();
            BikeDTO dto = dao.findByPrimaryKey(proId);

            txtBikeID.setText(dto.getBikeID());
            txtBikeName.setText(dto.getBikeName());
            txtManu.setText(dto.getManufacturer());
            txtModel.setText(dto.getModel());
            txtPrice.setText(String.valueOf(dto.getPrice()));
            txtYear.setText(dto.getReleasedYear());
            

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblViewBikeMouseClicked

    private void btnAddActionPerformed(ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        BikeDAO dao = new BikeDAO();
        String id = txtBikeID.getText();
        String name = txtBikeName.getText();
        String manu = txtManu.getText();
        String model = txtModel.getText();
        float price = Float.parseFloat(txtPrice.getText());
        String year = txtYear.getText();
        String valid = "";
        if (id.equals("")) {
            valid += "ID can't be blank \n";

        }
        if (name.equals("")) {
            valid += "Name can't be blank \n";
        }
        if (manu.equals("")) {
            valid += "Manufacturer can't be blank \n";
        }
        if (model.equals("")) {
            valid += "Model can't be blank \n";
        }
        if (year.equals("")) {
            valid += "Released Year can't be blank \n";
        }

        if (valid.equals("")) {
            try {
                if (dao.insert(id, name, manu, model, price, year)) {
                    JOptionPane.showMessageDialog(this, "Insert Success");
                    viewProduct();
                } else {
                    JOptionPane.showMessageDialog(this, "Insert False");
                }
            } catch (Exception e) {
                e.printStackTrace();
                if (e.getMessage().contains("duplicate")) {
                    JOptionPane.showMessageDialog(this, id + "is exsit");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, valid);
        }
    }//GEN-LAST:event_btnAddActionPerformed

